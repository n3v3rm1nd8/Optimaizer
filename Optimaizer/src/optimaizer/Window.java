    public static void rebootService() {
        try {
            // Ejecuta el comando de reinicio en Windows
            Process p  = Runtime.getRuntime().exec("taskkill /f /im explorer.exe");
            p.waitFor();
            Runtime.getRuntime().exec("cmd /c start explorer.exe");
        } catch (IOException e) {
            e.printStackTrace();
        } catch (InterruptedException ex) {
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public static String getCurrentUserSID() {
        String username = System.getProperty("user.name");  // Obtener el nombre de usuario actual
        String sid = null;

        try {
            // Ejecutar el comando wmic para obtener el SID del usuario actual
            Process process = new ProcessBuilder("wmic", "useraccount", "where", "name='" + username + "'", "get", "sid").start();
            BufferedReader reader = new BufferedReader(new InputStreamReader(process.getInputStream()));

            String line;
            while ((line = reader.readLine()) != null) {
                if (line.trim().startsWith("S-1-")) {  // El SID comienza con "S-1-"
                    sid = line.trim();  // Almacenar el SID
                    break;
                }
            }
            process.waitFor();  // Esperar a que el proceso termine
        } catch (Exception e) {
            e.printStackTrace();
        }

        return sid;
    }

